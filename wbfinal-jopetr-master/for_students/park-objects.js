      h1.add(h2);
      h2.position.y-=0.4;
      h2.position.x-=0.16;
      h2.position.z-=0.26;
      let h3 = new T.Mesh(hg2, opole_mat);
      h1.add(h3);
      h3.position.y-=0.4;
      h3.position.x-=0.16;
