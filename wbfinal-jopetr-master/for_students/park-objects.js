/**
 * @typedef BirdProperties
 * @type {object}
 * @property {number} [x=0]
 * @property {number} [y=0]
 * @property {number} [z=0]
 * @property {number} [size=1]
 */
export class GrBird extends GrObject {
  /**
   * @param {BirdProperties} params
   */
  constructor(params = {}) {
    let width = 3;
    let bird = new T.Group();

    let base_geom = new T.BoxGeometry(1,1,5);
    let base_mat = new T.MeshStandardMaterial({
      color: "white",
      metalness: 0.3,
      roughness: 0.8
    });
    let base = new T.Mesh(base_geom, base_mat);
    base.translateY(0.5);
    bird.add(base);
    let wg = new T.BoxGeometry(6, 0.5, 4);
    let w1 = new T.Mesh(wg, base_mat);
    base.add(w1);
    w1.position.x+=3
  
    let w2 = new T.Mesh(wg, base_mat);
    base.add(w2);
    w2.position.x-=3
    

   

    // note that we have to make the Object3D before we can call
    // super and we have to call super before we can use this
    super(`Bird-${birdObCtr++}`, bird);
    this.whole_ob = bird;
    this.base = base;
    this.u = 0;
    this.d = 0;
    this.w1 = w1;
    this.w2 = w2;

    // put the object in its place
    this.whole_ob.position.x = params.x ? Number(params.x) : 0;
    this.whole_ob.position.y = params.y ? Number(params.y) : 0;
    this.whole_ob.position.z = params.z ? Number(params.z) : 0;
    let scale = params.size ? Number(params.size) : 1;
    bird.scale.set(scale, scale, scale);

    
  }
  /**
   * StepWorld method
   * @param {*} delta 
   * @param {*} timeOfDay 
   */
  stepWorld(delta, timeOfDay) {
    if (this.d==0 && this.u<1) {
      this.u+=0.01;
    }
    else if (this.d==1 && this.u>-1) {
      this.u-=0.01;
    }
    else if (this.d==0 && this.u>=1){
      this.d = 1;
      this.u -= 0.01;
    }
    else {
      this.d = 0;
      this.u+=0.01;
    }
    this.w1.position.y=2.8*this.u;
    this.w1.position.x=3-Math.abs(this.u);
    this.w2.position.x=-this.w1.position.x;
    this.w1.rotation.z = this.u;
    this.w2.rotation.z = -this.u;
    this.w2.position.y=this.w1.position.y

    let theta = Math.PI*(this.u+1);
    if (this.d==0) {
      theta = -Math.PI*(this.u+1);
    }
    let x = 3 * Math.cos(theta);
    let z = 3 * Math.sin(theta);
    let hyp = Math.sqrt(Math.pow(x, 2) + Math.pow(z, 2));
    let dx = x/hyp;
    let dz = z/hyp;
    let ang = 0;
    if (dz<=0) {
        ang = Math.acos(dx);
    }
    else {
        ang = 2*Math.PI - Math.acos(dx);
    }
    this.base.position.x = 10*x;
    this.base.position.z = 10*z;
    this.base.rotation.y = Math.PI+ang;

  }
}

